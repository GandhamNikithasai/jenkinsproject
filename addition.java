public class addition {

    public static void main(String[] args) {
        // Hardcoded values for the two numbers
        int number1 = 10;
        int number2 = 20;
       
        // Performing addition
        int sum = number1 + number2;
       
        // Displaying the result
        System.out.println("The sum of " + number1 + " and " + number2 + " is: " + sum);
    }
}

